    private void jButton6archivotxt2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6archivotxt2ActionPerformed
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if (x == JFileChooser.APPROVE_OPTION) {
            path = j.getSelectedFile().getPath();
        }
        try {
            FileWriter fw = new FileWriter(path + "DATOS PERSONALES.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            for (int i = 0; i < tabla1.getRowCount(); i++) {
                String LN = tabla1.getValueAt(i, 1).toString();
                String MN = tabla1.getValueAt(i, 2).toString();
                String QN = tabla1.getValueAt(i, 3).toString();
                String PN = tabla1.getValueAt(i, 4).toString();
                String KN = tabla1.getValueAt(i, 5).toString();
                String AN = tabla1.getValueAt(i, 6).toString();
                String EN = tabla1.getValueAt(i, 7).toString();
                String WN = tabla1.getValueAt(i, 8).toString();
                String ÑN = tabla1.getValueAt(i, 9).toString();
                bw.write(LN + "\t" + MN + "\t" + QN + "\t" + PN + "\t" + KN + "\t" + AN + "\t" + EN + "\t" + WN + "\t" + ÑN);
                bw.newLine();
            }
            bw.close();
            JOptionPane.showMessageDialog(null, "Archivo TXT generado");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton6archivotxt2ActionPerformed

    private void pdftabla2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pdftabla2ActionPerformed

        // en esta linea implemente Significa que estás obteniendo la
        //ruta del directorio seleccionado por el usuario en un cuadro de diálogo
        //de selección de directorio (JFileChooser) y asignándola a una variable llamada variablepdf.
        String variablepdf = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if (x == JFileChooser.APPROVE_OPTION) {
            variablepdf = j.getSelectedFile().getPath();
        }
        Document doc = new Document(new Rectangle(1200, 2000));
        try {
            PdfWriter writer = PdfWriter.getInstance(doc, new FileOutputStream(variablepdf + "abelito.pdf"));
            // Implementar la clase PdfPageEventHelper
            writer.setPageEvent(new PdfPageEventHelper() {
                @Override
                public void onEndPage(PdfWriter writer, Document document) {
                    // Rotar la página 360 grados en sentido antihorario
                    writer.addPageDictEntry(PdfName.ROTATE, new PdfNumber(360));
                }
            });
            doc.open();
            PdfPTable tbl = new PdfPTable(9);
            tbl.addCell("nombre");
            tbl.addCell("apellido");
            tbl.addCell("edad");
            tbl.addCell("acudiente");
            tbl.addCell("grado");
            tbl.addCell("correo");
            tbl.addCell("documento");
            tbl.addCell("f nacimiento");
            tbl.addCell("celular");

            for (int i = 0; i < tabla1.getRowCount(); i++) {

                String LN = tabla1.getValueAt(i, 1).toString();
                String MN = tabla1.getValueAt(i, 2).toString();
                String QN = tabla1.getValueAt(i, 3).toString();
                String PN = tabla1.getValueAt(i, 4).toString();
                String KN = tabla1.getValueAt(i, 5).toString();
                String AN = tabla1.getValueAt(i, 6).toString();
                String EN = tabla1.getValueAt(i, 7).toString();
                String WN = tabla1.getValueAt(i, 8).toString();
                String ÑN = tabla1.getValueAt(i, 9).toString();
                tbl.addCell(LN);
                tbl.addCell(MN);
                tbl.addCell(QN);
                tbl.addCell(PN);
                tbl.addCell(KN);
                tbl.addCell(AN);
                tbl.addCell(EN);
                tbl.addCell(WN);
                tbl.addCell(ÑN);
            }
            doc.add(tbl);
            JOptionPane.showMessageDialog(null, "PDF GENERADO");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "no se pudo generar el mensaje");
        } finally {
            if (doc != null) {
                doc.close();
            }
        }
    }//GEN-LAST:event_pdftabla2ActionPerformed

