    void consultar() {
        String sql = "select * from alumno ";
        try {
            conet = con2.getConnection2();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] alumno = new Object[10];
            modelo = (DefaultTableModel) tabla1.getModel();
            while (rs.next()) {
                alumno[0] = rs.getInt("id");
                alumno[1] = rs.getString("nombre");
                alumno[2] = rs.getString("apellido");
                alumno[3] = rs.getInt("edad");
                alumno[4] = rs.getString("acudiente");
                alumno[5] = rs.getString("grado");
                alumno[6] = rs.getString("correo");
                alumno[7] = rs.getString("identidad");
                alumno[8] = rs.getString("fecha");
                alumno[9] = rs.getString("celular");

                modelo.addRow(alumno);
            }
            tabla1.setModel(modelo);

        } catch (Exception e) {
        }
    }

